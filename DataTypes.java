
public class DataTypes {

	public static void main (String[] args) {
		
		int i=111;
		float f = 11.1f;
		double d= 111.111;
		char c = 'C';
		boolean b = false;
		
		System.out.println("This is an Integer "+i);
		System.out.println("This is a Float "+f);
		System.out.println("This is an Duble "+d);
		System.out.println("This is an Char "+c);
		System.out.println("This is an Boolean "+b);
	}
}
